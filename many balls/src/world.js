let track = [];

function getGridCellAtIndex(row, column) {
	if(row >= 0 && row < ROWS && column >= 0 &&
	   column < COLUMNS) {
		return track[row][column];
	}
	return null;
}

function drawWorld() {
	let x = 0, y = 0;
	for(let row = 0; row < ROWS; row++) {
		x = 0;
		for(let column = 0; column < COLUMNS; column++) {
			let tile = getGridCellAtIndex(row, column);
			if (tile == WALL) {
				drawImage(tileSprites[tile], x, y);
			}
			else {
				drawColoredRect(x, y, TILE_WIDTH, TILE_HEIGHT, "black");
			}
			x += TILE_WIDTH
		}
		x = 0;
		y += TILE_HEIGHT;
	}
}
